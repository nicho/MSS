package com.aus.common.model;

public class FlexValueDto {
	String flexValue;
	String flexValuesDesc;
	String description;
	String parentFlexValue;
	String flexValueSetName;
	

	

	public String getFlexValueSetName() {
		return flexValueSetName;
	}

	public void setFlexValueSetName(String flexValueSetName) {
		this.flexValueSetName = flexValueSetName;
	}

	public String getParentFlexValue() {
		return parentFlexValue;
	}

	public void setParentFlexValue(String parentFlexValue) {
		this.parentFlexValue = parentFlexValue;
	}

	public String getDescription() {
		return description;
	}

	public void setDescription(String description) {
		this.description = description;
	}

	public String getFlexValue() {
		return flexValue;
	}

	public void setFlexValue(String flexValue) {
		this.flexValue = flexValue;
	}

	public String getFlexValuesDesc() {
		return flexValuesDesc;
	}

	public void setFlexValuesDesc(String flexValuesDesc) {
		this.flexValuesDesc = flexValuesDesc;
	}
 
}
