package com.aus.common.model;

public class StoreInfo {
	String storeCode;
	String storeName;
	String mainSubinvCode;
	String status;
	String ouId;
	String subinv_code;
 

	public String getSubinv_code() {
		return subinv_code;
	}

	public void setSubinv_code(String subinv_code) {
		this.subinv_code = subinv_code;
	}

	public String getStatus() {
		return status;
	}

	public void setStatus(String status) {
		this.status = status;
	}

	public String getOuId() {
		return ouId;
	}

	public void setOuId(String ouId) {
		this.ouId = ouId;
	}

	public String getStoreCode() {
		return storeCode;
	}

	public void setStoreCode(String storeCode) {
		this.storeCode = storeCode;
	}

	public String getStoreName() {
		return storeName;
	}

	public void setStoreName(String storeName) {
		this.storeName = storeName;
	}

	public String getMainSubinvCode() {
		return mainSubinvCode;
	}

	public void setMainSubinvCode(String mainSubinvCode) {
		this.mainSubinvCode = mainSubinvCode;
	}

}
